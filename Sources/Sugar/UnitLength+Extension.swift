import Foundation
import UIKit

public extension UnitLength {
  /// The metres unit of length.
  class var metres: UnitLength {
    return meters
  }
  
  /// The centimetres unit of length.
  class var centimetres: UnitLength {
    return centimeters
  }
  
  /// The kilometres unit of length.
  class var kilometres: UnitLength {
    return kilometers
  }
  
  /// The millimetres unit of length.
  class var millimetres: UnitLength {
    return millimeters
  }
}
