import Foundation

public extension UnitVolume {
  /// The megalitres unit of volume.
  class var megalitres: UnitVolume {
    return self.megaliters
  }
  
  /// The kilolitres unit of volume.
  class var kilolitres: UnitVolume {
    return self.kiloliters
  }
  
  /// The litres unit of volume.
  class var litres: UnitVolume {
    return self.liters
  }
  
  /// The decilitres unit of volume.
  class var decilitres: UnitVolume {
    return self.deciliters
  }
  
  /// The centilitres unit of volume.
  class var centilitres: UnitVolume {
    return self.centiliters
  }
  
  /// The millilitres unit of volume.
  class var millilitres: UnitVolume {
    return self.milliliters
  }
  
  /// The cubic kilometres unit of volume.
  class var cubicKilometres: UnitVolume {
    return self.cubicKilometers
  }
  
  /// The cubic metres unit of volume.
  class var cubicMetres: UnitVolume {
    return self.cubicMeters
  }
  
  /// The cubic decimetres unit of volume.
  class var cubicDecimetres: UnitVolume {
    return self.cubicDecimeters
  }
  
  /// The cubic centimetres unit of volume.
  class var cubicCentimetres: UnitVolume {
    return self.cubicCentimeters
  }
  
  /// The cubic millimetres unit of volume.
  class var cubicMillimetres: UnitVolume {
    return self.cubicMillimeters
  }
}
