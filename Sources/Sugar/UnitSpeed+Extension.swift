import Foundation

public extension UnitSpeed {
  /// The metre per second unit of speed.
  class var metresPerSecond: UnitSpeed {
    return self.metersPerSecond
  }
  
  /// The kilometres per hour unit of speed.
  class var kilometresPerHour: UnitSpeed {
    return self.kilometersPerHour
  }
}
