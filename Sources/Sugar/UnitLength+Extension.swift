import Foundation
import UIKit

public extension UnitLength {
  /// The megametres unit of length.
  class var megametres: UnitLength {
    return self.megameters
  }
  
  /// The kilometres unit of length.
  class var kilometres: UnitLength {
    return self.kilometers
  }
  
  /// The hectometres unit of length.
  class var hectometres: UnitLength {
    return self.hectometers
  }
  
  /// The decametres unit of length.
  class var decametres: UnitLength {
    return self.decameters
  }
  
  /// The metres unit of length.
  class var metres: UnitLength {
    return self.meters
  }
  
  /// The decimetres unit of length.
  class var decimetres: UnitLength {
    return self.decimeters
  }
  
  /// The centimetres unit of length.
  class var centimetres: UnitLength {
    return self.centimeters
  }
  
  /// The millimetres unit of length.
  class var millimetres: UnitLength {
    return self.millimeters
  }
  
  /// The micrometres unit of length.
  class var micrometres: UnitLength {
    return self.micrometers
  }
  
  /// The nanometres unit of length.
  class var nanometres: UnitLength {
    return self.nanometers
  }
  
  /// The picometres unit of length.
  class var picometres: UnitLength {
    return self.picometers
  }
}
